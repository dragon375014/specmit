// awaken — scan a scope (global ~/.claude, or a project folder) and write an
// ALWAYS-LOADED resource index so every new Claude session "wakes up" already
// knowing which skills / ecosystem repos / MCP servers / memory it can use.
//
// Why this exists: `specmit init` drops files that are NOT on Claude's
// every-session load path (project CLAUDE.md / project .claude/skills / memory),
// so a fresh machine feels "dumb". awaken writes a compact, ACTIVE section into
// the one file Claude reads at the start of every conversation (CLAUDE.md) — so
// the very next session is visibly different. The "feel" comes from what gets
// written into CLAUDE.md, not from the tool itself.
//
// Tier-1, deterministic, zero-LLM, re-runnable. Tier-2 (a curation skill that
// enriches the raw index) is a separate, later piece.

import { existsSync, readFileSync, writeFileSync, readdirSync } from 'node:fs'
import { join } from 'node:path'
import { homedir } from 'node:os'

// ── minimal UI (self-contained; pipeline.js keeps its own copy) ──
const B = s => `\x1b[1m${s}\x1b[0m`
const G = s => `\x1b[32m${s}\x1b[0m`
const Y = s => `\x1b[33m${s}\x1b[0m`
const D = s => `\x1b[2m${s}\x1b[0m`

const TODAY = new Date().toISOString().slice(0, 10)
const MARK_BEGIN = '<!-- AWAKEN:BEGIN — auto-generated by `npx specmit awaken`; regenerate, do not hand-edit -->'
const MARK_END = '<!-- AWAKEN:END -->'

