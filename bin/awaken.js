// ─── scanners (all best-effort; a failure degrades to empty, never crashes) ───

function clip(s, n) {
  if (!s) return ''
  return s.length > n ? s.slice(0, n - 1) + '…' : s
}

// Parse the YAML `description:` from skill frontmatter. Robust to CRLF line
// endings and to block scalars (`description: |` / `>` with the text on
// following indented lines). Returns whitespace-collapsed text (cap 240).
function readFrontmatterDesc(text) {
  const t = text.replace(/\r\n/g, '\n').replace(/\r/g, '\n')
  const m = t.match(/^---\n([\s\S]*?)\n---/)
  if (!m) return ''
  const lines = m[1].split('\n')
  let desc = ''
  for (let i = 0; i < lines.length; i++) {
    const dm = lines[i].match(/^description:\s*(.*)$/)
    if (!dm) continue
    let val = dm[1].trim()
    if (/^[|>][-+]?$/.test(val) || val === '') {
      // block scalar (or empty): collect subsequent indented / blank lines
      const collected = []
      for (let j = i + 1; j < lines.length; j++) {
        if (/^\s+\S/.test(lines[j])) collected.push(lines[j].trim())
        else if (lines[j].trim() === '') collected.push('')
        else break // dedent → next top-level key
      }
      val = collected.join(' ')
    } else {
      val = val.replace(/^["']|["']$/g, '')
    }
    desc = val
    break
  }
  return clip(desc.replace(/\s+/g, ' ').trim(), 240)
}

function scanSkills(dir) {
  const out = []
  try {
    if (!existsSync(dir)) return out
    for (const e of readdirSync(dir, { withFileTypes: true })) {
      if (!e.isDirectory()) continue
      const md = join(dir, e.name, 'SKILL.md')
      if (!existsSync(md)) continue
      out.push({ name: e.name, desc: readFrontmatterDesc(readFileSync(md, 'utf8')) })
    }
  } catch { /* degrade */ }
  return out.sort((a, b) => a.name.localeCompare(b.name))
}

function findEcosystemRepos() {
  // build a name → path index across the search roots (depth 1)
  const present = new Map()
  for (const root of SEARCH_ROOTS) {
    try {
      if (!existsSync(root)) continue
      for (const e of readdirSync(root, { withFileTypes: true })) {
        if (e.isDirectory() && !present.has(e.name)) present.set(e.name, join(root, e.name))
      }
    } catch { /* skip root */ }
  }
  return ECOSYSTEM_REPOS.map(r => {
    let foundAt = null
    for (const n of r.dirNames) { if (present.has(n)) { foundAt = present.get(n); break } }
    return { ...r, foundAt }
  })
}

function scanMcp() {
  const out = new Set()
  try {
    const p = join(homedir(), '.claude.json')
    if (!existsSync(p)) return []
    const j = JSON.parse(readFileSync(p, 'utf8'))
    for (const k of Object.keys(j.mcpServers || {})) out.add(k)
    for (const proj of Object.values(j.projects || {})) {
      for (const k of Object.keys((proj && proj.mcpServers) || {})) out.add(k)
    }
  } catch { /* degrade */ }
  return [...out].sort()
}

function scanGlobalMemory() {
  try {
    const base = join(homedir(), '.claude', 'projects')
    if (!existsSync(base)) return { projects: 0, withMemory: 0 }
    const dirs = readdirSync(base, { withFileTypes: true }).filter(d => d.isDirectory())
    let withMemory = 0
    for (const d of dirs) {
      if (existsSync(join(base, d.name, 'memory', 'MEMORY.md'))) withMemory++
    }
    return { projects: dirs.length, withMemory }
  } catch { return { projects: 0, withMemory: 0 } }
}

function listFiles(dir, re) {
  try {
    if (!existsSync(dir)) return []
    return readdirSync(dir, { withFileTypes: true })
      .filter(e => e.isFile() && re.test(e.name)).map(e => e.name)
  } catch { return [] }
}

function countFiles(dir, re) { return listFiles(dir, re).length }

function readPkg(cwd) {
  try {
    const p = join(cwd, 'package.json')
    if (!existsSync(p)) return null
    const j = JSON.parse(readFileSync(p, 'utf8'))
    return { name: j.name || '(unnamed)', scripts: Object.keys(j.scripts || {}) }
  } catch { return null }
}

const REGISTRY_RE = /(registry|capabilities|ecosystem|work-?board|backlog|tech-debt)/i

function findRegistries(cwd) {
  const out = []
  const roots = [cwd, join(cwd, '文檔'), join(cwd, 'docs'), join(cwd, 'doc')]
  for (const root of roots) {
    try {
      if (!existsSync(root)) continue
      for (const e of readdirSync(root, { withFileTypes: true })) {
        if (e.isFile() && e.name.endsWith('.md') && REGISTRY_RE.test(e.name)) {
          const rel = root === cwd ? e.name : `${root.split(/[\\/]/).pop()}/${e.name}`
          out.push(rel)
        }
      }
    } catch { /* skip */ }
  }
  return [...new Set(out)].slice(0, 12)
}

function findDocsDirs(cwd) {
  const out = []
  for (const name of ['文檔', 'docs', 'doc']) {
    const dir = join(cwd, name)
    try {
      if (!existsSync(dir)) continue
      const mdCount = readdirSync(dir, { withFileTypes: true })
        .filter(e => (e.isFile() && e.name.endsWith('.md'))).length
      const subDirs = readdirSync(dir, { withFileTypes: true }).filter(e => e.isDirectory()).length
      out.push({ name, mdCount, subDirs })
    } catch { /* skip */ }
  }
  return out
}

function scanProject(cwd) {
  return {
    cwd,
    skills: scanSkills(join(cwd, '.claude', 'skills')),
    workflows: listFiles(join(cwd, '.claude', 'workflows'), /\.(js|mjs)$/),
    memory: existsSync(join(cwd, 'memory', 'MEMORY.md')),
    pkg: readPkg(cwd),
    migrations: countFiles(join(cwd, 'supabase', 'migrations'), /\.sql$/) || countFiles(join(cwd, 'migrations'), /\.sql$/),
    registries: findRegistries(cwd),
    docs: findDocsDirs(cwd),
  }
}

// ─── rendering ───

function wrapBlock(lines) {
  return [MARK_BEGIN, '', ...lines, '', MARK_END].join('\n')
}

function renderGlobalClaudeSection(d) {
  const core = d.skills.filter(s => EXPECTED_GLOBAL_SKILLS.includes(s.name))
  const others = d.skills.filter(s => !EXPECTED_GLOBAL_SKILLS.includes(s.name))
  const L = []
  L.push(`## 🌅 這台機器有哪些資源（specmit awaken 自動盤點 ${TODAY}）`)
  L.push('')
  L.push('> **每個新對話開場第一件事**：掃一眼這份清單，主動讓使用者知道有哪些資源可用；任務匹配時優先用對應的 skill / 工具 / repo，不要從零開始。完整索引見 `~/.claude/RESOURCES.md`。')
  L.push('')
  L.push(`**全域 skill（共 ${d.skills.length} 支，說對應的話會自動觸發）**`)
  if (core.length) {
    L.push('生態系核心：')
    for (const s of core) L.push(`- **${s.name}** — ${clip(s.desc, 90) || '(無描述)'}`)
  }
  if (others.length) {
    const names = others.map(s => s.name).join('、')
    L.push(`其餘 ${others.length} 支：${names}`)
  }
  const present = new Set(d.skills.map(s => s.name))
  const missing = EXPECTED_GLOBAL_SKILLS.filter(n => !present.has(n))
  if (missing.length) {
    L.push(`⚠ 預期該有但本機缺：${missing.join('、')}（補裝：\`npx specmit init\`）`)
  }
  L.push('')
  L.push('**生態系 repo（dragon375014 工具鏈，本機是否已 clone）**')
  for (const r of d.repos) {
    if (r.foundAt) L.push(`- ✅ **${r.key}** — ${r.role}　\`${r.foundAt}\``)
    else L.push(`- ❌ **${r.key}** — ${r.role}（本機未 clone，需要時：\`${r.clone}\`）`)
  }
  L.push('')
  L.push(`**MCP 伺服器**：${d.mcp.length ? d.mcp.join('、') : '（無偵測到）'}`)
  L.push(`**記憶（memory）**：${d.mem.withMemory}/${d.mem.projects} 個專案有 MEMORY.md 索引`)
  L.push('')
  L.push('**怎麼用**：規劃新專案 → 說「我想做一個…」（idea-to-spec）；一條龍跑完 → 「跑完整管線」（specmit）；同一檢查跑多個單位 → workflow-shaper；單一任務打磨 → goal；想擋住重複的 bug 類 → governance-meta。')
  return wrapBlock(L)
}

function renderProjectClaudeSection(d) {
  const L = []
  const projName = d.pkg ? d.pkg.name : (d.cwd.split(/[\\/]/).pop() || 'this project')
  L.push(`## 🌅 這個專案有哪些資源（specmit awaken 自動盤點 ${TODAY}）`)
  L.push('')
  L.push('> **每個新對話開場第一件事**：掃一眼這份清單，主動讓使用者知道本專案有哪些資源可查；任務匹配時優先用對應 skill / 讀對應索引，不要從零摸索。完整索引見 `./RESOURCES.md`。')
  L.push('')
  L.push(`**專案 skill（共 ${d.skills.length} 支，自動觸發）**`)
  if (d.skills.length) {
    const shown = d.skills.slice(0, 12)
    for (const s of shown) L.push(`- **${s.name}** — ${clip(s.desc, 90) || '(無描述)'}`)
    if (d.skills.length > shown.length) L.push(`- …另 ${d.skills.length - shown.length} 支見 \`./RESOURCES.md\``)
  } else {
    L.push('- （無 `.claude/skills/`）')
  }
  L.push('')
  if (d.registries.length) {
    L.push('**索引 / SSOT 檔（先讀這些再動手）**')
    for (const r of d.registries) L.push(`- \`${r}\``)
    L.push('')
  }
  const facts = []
  if (d.docs.length) facts.push(d.docs.map(x => `${x.name}/（${x.mdCount} 篇 md・${x.subDirs} 子資料夾）`).join('、'))
  if (d.memory) facts.push('有 `memory/MEMORY.md`（每次必載）')
  if (d.migrations) facts.push(`${d.migrations} 個 migration`)
  if (d.workflows.length) facts.push(`workflows：${d.workflows.join('、')}`)
  if (d.pkg && d.pkg.scripts.length) facts.push(`npm scripts：${d.pkg.scripts.slice(0, 10).join('、')}`)
  if (facts.length) { L.push('**其他資源**'); for (const f of facts) L.push(`- ${f}`) ; L.push('') }
  L.push(`**專案**：${projName}`)
  return wrapBlock(L)
}

function renderResourcesMd(scope, d) {
  const L = []
  L.push(`# RESOURCES — ${scope === 'global' ? '這台機器' : '本專案'}的資源索引`)
  L.push('')
  L.push(`> 由 \`npx specmit awaken${scope === 'global' ? '' : ' --project'}\` 自動盤點生成（${TODAY}）。`)
  L.push('> 這是給 Claude / 你自己「先讀索引、再深掃選中項」用的 Layer-0 速查表。可隨時重跑刷新。')
  L.push('')
  if (scope === 'global') {
    L.push(`## 全域 skill（${d.skills.length} 支）`)
    L.push('')
    for (const s of d.skills) L.push(`- **${s.name}** — ${s.desc || '(無描述)'}`)
    L.push('')
    L.push('## 生態系 repo')
    L.push('')
    for (const r of d.repos) {
      L.push(`- ${r.foundAt ? '✅' : '❌'} **${r.key}** — ${r.role}`)
      L.push(`  - ${r.foundAt ? `路徑：\`${r.foundAt}\`` : `未在本機：\`${r.clone}\``}`)
    }
    L.push('')
    L.push('## MCP 伺服器')
    L.push('')
    L.push(d.mcp.length ? d.mcp.map(m => `- ${m}`).join('\n') : '- （無偵測到）')
    L.push('')
    L.push('## 記憶（memory）')
    L.push('')
    L.push(`- ${d.mem.withMemory}/${d.mem.projects} 個專案有 \`memory/MEMORY.md\` 索引（位於 \`~/.claude/projects/*/memory/\`）`)
  } else {
    L.push(`## 專案 skill（${d.skills.length} 支）`)
    L.push('')
    for (const s of d.skills) L.push(`- **${s.name}** — ${s.desc || '(無描述)'}`)
    L.push('')
    if (d.registries.length) {
      L.push('## 索引 / SSOT 檔'); L.push('')
      for (const r of d.registries) L.push(`- \`${r}\``)
      L.push('')
    }
    if (d.docs.length) {
      L.push('## 文檔資料夾'); L.push('')
      for (const x of d.docs) L.push(`- \`${x.name}/\` — ${x.mdCount} 篇 md、${x.subDirs} 個子資料夾`)
      L.push('')
    }
    L.push('## 其他')
    L.push('')
    L.push(`- 記憶：${d.memory ? '有 `memory/MEMORY.md`' : '無'}`)
    L.push(`- migration：${d.migrations} 個`)
    L.push(`- workflows：${d.workflows.length ? d.workflows.join('、') : '無'}`)
    if (d.pkg) L.push(`- package：${d.pkg.name}（scripts：${d.pkg.scripts.join('、') || '無'}）`)
  }
  L.push('')
  return L.join('\n')
}

function upsertClaudeMd(path, block) {
  let existing = ''
  try { if (existsSync(path)) existing = readFileSync(path, 'utf8') } catch { /* new file */ }
  let next, action
  if (existing.includes(MARK_BEGIN) && existing.includes(MARK_END)) {
    const re = new RegExp(`${MARK_BEGIN.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}[\\s\\S]*?${MARK_END.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}`)
    next = existing.replace(re, block)
    action = 'updated'
  } else if (existing.trim()) {
    next = existing.replace(/\s*$/, '') + '\n\n' + block + '\n'
    action = 'appended'
  } else {
    next = block + '\n'
    action = 'created'
  }
  writeFileSync(path, next, 'utf8')
  return action
}

// ─── command ───

export async function cmdAwaken(argv) {
  const isProject = argv.includes('--project')
  const dry = argv.includes('--dry') || argv.includes('--dry-run')
  const scope = isProject ? 'project' : 'global'

  console.log(`\n${B('specmit')} — awaken  (${scope}${dry ? Y(' [--dry]') : ''})`)

  let data, claudeMdPath, resourcesPath, claudeSection
  if (scope === 'global') {
    const base = join(homedir(), '.claude')
    data = {
      skills: scanSkills(join(base, 'skills')),
      repos: findEcosystemRepos(),
      mcp: scanMcp(),
      mem: scanGlobalMemory(),
    }
    claudeMdPath = join(base, 'CLAUDE.md')
    resourcesPath = join(base, 'RESOURCES.md')
    claudeSection = renderGlobalClaudeSection(data)
    console.log(D(`   skills ${data.skills.length}・repos ${data.repos.filter(r => r.foundAt).length}/${data.repos.length} present・mcp ${data.mcp.length}・memory ${data.mem.withMemory}/${data.mem.projects}`))
  } else {
    const cwd = process.cwd()
    data = scanProject(cwd)
    claudeMdPath = join(cwd, 'CLAUDE.md')
    resourcesPath = join(cwd, 'RESOURCES.md')
    claudeSection = renderProjectClaudeSection(data)
    console.log(D(`   skills ${data.skills.length}・registries ${data.registries.length}・migrations ${data.migrations}・memory ${data.memory ? 'yes' : 'no'}`))
  }

  const resourcesMd = renderResourcesMd(scope, data)

  if (dry) {
    console.log(`\n${Y('— DRY RUN — would write:')}`)
    console.log(D(`\n[${claudeMdPath}]  (AWAKEN section)`))
    console.log(claudeSection)
    console.log(D(`\n[${resourcesPath}]`))
    console.log(resourcesMd)
    console.log(`\n${Y('Nothing written (--dry).')}`)
    return
  }

  const action = upsertClaudeMd(claudeMdPath, claudeSection)
  writeFileSync(resourcesPath, resourcesMd, 'utf8')

  console.log(`\n${G('✅')} CLAUDE.md ${action}  →  ${claudeMdPath}`)
  console.log(`${G('✅')} RESOURCES.md written  →  ${resourcesPath}`)
  console.log(`\n${B('有感驗收')}：開一個新的 Claude Code 對話，打「嗨」——它應該一開口就報出你有哪些資源。`)
  if (scope === 'global') {
    const missing = data.repos.filter(r => !r.foundAt && r.key !== 'reuse-hub')
    if (missing.length) {
      console.log(`\n${Y('缺的 ecosystem repo（需要時 clone）：')}`)
      for (const r of missing) console.log(`   ${r.clone}`)
    }
    console.log(`\n${D('提示：在某個專案資料夾裡跑 `npx specmit awaken --project` 可盤點該專案。')}`)
  }
  console.log('')
}
